#include "ft_printf.h"

int	fct2(size_t n)
{
	int		i;
	char	*base;

	i = 0;
	base = "0123456789ABCDEF";
	if (n > 15)
	{
		i += fct2 (n / 16);
		i += fct2 (n % 16);
	}
	else
		i += ft_putchar(base[n]);
	return (i);
}
