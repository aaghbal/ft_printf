#include "ft_printf.h"

int	ft_putnbr_u(unsigned int n)
{
	int	i;

	i = 0;
	if (n > 9)
	{
		i += ft_putnbr_u(n / 10);
		i += ft_putnbr_u(n % 10);
	}
	else
	{
		i += ft_putchar(n + '0');
	}
	return (i);
}
